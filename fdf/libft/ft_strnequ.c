#include "libft.h"

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	size_t	i;
	size_t	a;

	i = 0;
	a = 0;
	while (s1[i] && s2[i] && i < n)
	{
		if (s1[i] == s2[i])
			a++;
		i++;
	}
	if (a == i)
		return (1);
	return (0);
}
