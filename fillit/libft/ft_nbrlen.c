#include "libft.h"

size_t	ft_nbrlen(int n)
{
	size_t i;

	i = 0;
	if (n < 0)
		i++;
	while (n / 10 != 0)
	{
		n = n / 10;
		i++;
	}
	i++;
	return (i);
}
