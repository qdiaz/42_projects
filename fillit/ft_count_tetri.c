#include "fillit.h"

int	ft_count_tetri(char *s)
{
	int cnt;

	cnt = 1;
	while (*s)
	{
		if (*s == '\n' && *(s + 1) == '\n')
			cnt++;
		s++;
	}
	return (cnt);
}
