#include "libft.h"

int		ft_strequ(char const *s1, char const *s2)
{
	int i;
	int a;

	i = 0;
	a = 0;
	while (s1[i] && s2[i])
	{
		if (s1[i] == s2[i])
			a++;
		i++;
	}
	if (a == i)
		return (1);
	return (0);
}
