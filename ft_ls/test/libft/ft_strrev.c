#include "libft.h"

char	*ft_strrev(char *s)
{
	int		end;
	int		start;
	char	tmp;

	start = 0;
	end = ft_strlen(s);
	while (start < end)
	{
		tmp = s[start];
		s[start] = s[end];
		s[end] = tmp;
		start++;
		end--;
	}
	return (s);
}
